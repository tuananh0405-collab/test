package model;

/**
 *
 * @author Admin
 */
public class User {

    private int id;
    private String name;
    private String email;
    private String password;
    private int roleid;
    private String phone;
    private String image;

    public User() {
    }

    public User(int id, String name, String email, String password, int roleid, String phone, String image) {
        this.id = id;
        this.name = name;
        this.email = email;
        this.password = password;
        this.roleid = roleid;
        this.phone = phone;
        this.image = image;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public int getRoleid() {
        return roleid;
    }

    public void setRoleid(int roleid) {
        this.roleid = roleid;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

}
